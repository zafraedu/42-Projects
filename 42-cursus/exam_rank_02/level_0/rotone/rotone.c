#include <unistd.h>

void	ft_putchar(char c){
	write(1, &c, 1);
}

void	rotone(char *s)
{
	while(*s)
	{
		if ((*s >= 'A' && *s <= 'Y') || (*s >= 'a' && *s <= 'y'))
			ft_putchar(*s + 1);
		else if (*s == 'Z' || *s == 'z')
			ft_putchar(*s - 25);
		else
			ft_putchar(*s);
		s++;
	}
}

int	main(int ac, char **av){
	if (ac == 2)
		rotone(av[1]);
	write(1, "\n", 1);
	return 0;
}
